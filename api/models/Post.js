const mongoose = require("mongoose");

// defining the postSchema
const PostSchema = new mongoose.Schema(
  {
    title: {
      type: String,
      required: true,
      unique: true,
    },
    desc: {
      type: String,
      required: true,
    },
    photo: {
      type: String,
      required: true,
    },
    username: {
      type: String,
      required: true,
    },
    categories: {
      type: Array,
      required: false,
    },
  },
  { timesstamps: true }
);

// exporting the mongoose model of post named User (ie the collection)
module.exports = mongoose.model("Post", PostSchema);
